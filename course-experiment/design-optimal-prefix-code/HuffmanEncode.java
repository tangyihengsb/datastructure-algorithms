/*
实验三
实验名称：
  最优前缀编码设计
实验目的：
  利用贪心算法设计最优前缀编码算法的编程实现
实验内容：
  用户输入n个正整数作为文件中n个字符出现的次数，请实现霍夫曼编码算法对该文件n个字符进行编码，并输出编码表。
实验原理：
  根据给定的n个权值{w1,w2,……wn}，构造n棵只有根结点的二叉树。
  在森林中选取两棵根结点权值最小的树作左右子树，构造一棵新的二叉树，置新二叉树根结点权值为其左右子树根结点权值之和。
  在森林中删除这两棵树，同时将新得到的二叉树加入森林中。重复上述两步，直到只含一棵树为止，这棵树即霍夫曼树。
  按左0右1的规定，从根结点走到一个叶结点，完成一个字符的译码。
*/
